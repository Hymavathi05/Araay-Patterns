public class Pattern2 {
       public void pattern2()
       {
    	   int n=3;
    	   for(int i=1;i<=n;i++)
    	   {
    		   System.out.print("* ");
    	   }
       }
	public static void main(String[] args) {
		Pattern2 p2=new Pattern2();
		p2.pattern2();                /*
		                               Output:
		                               * * * 
		                               */
	}

}
